package CapstoneProjectOOP;

public class Restaurant {
	//Data Fields/Attributes
	private String name;
    private String area;
    //Constructor
    public Restaurant(String name, String area) {
        this.area = name;
        this.area = area;
    }
    //Methods
    public String getName() {
    	return name;
    }

    public String getArea() {
        return area;
    }
}
